// Дана строка '2+3 223 2223'. 
// Напишите регулярку, которая найдет строку 2+3, 
// не захватив остальные.

console.log('2+3 223 2223'.replace(/2\+3/g, '!'));
// ---> ! 223 2223

//////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////

// Дана строка '23 2+3 2++3 2+++3 345 567'. 
// Напишите регулярку, которая найдет строки 2+3, 2++3, 2+++3, 
// не захватив остальные (+ может быть любое количество).

console.log('23 2+3 2++3 2+++3 345 567'.replace(/2\++3/g, '!'));
// ---> 23 ! ! ! 345 567

//////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////

// Дана строка '23 2+3 2++3 2+++3 445 677'. 
// Напишите регулярку, которая найдет строки 23, 2+3, 2++3, 2+++3, 
// не захватив остальные.

console.log('23 2+3 2++3 2+++3 445 677'.replace(/2\+*3/g, '!'));
// ---> ! ! ! ! 445 677

//////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////

// Дана строка '*+ *q+ *qq+ *qqq+ *qqq qqq+'. 
// Напишите регулярку, которая найдет строки *q+, *qq+, *qqq+, 
// не захватив остальные.

console.log('*+ *q+ *qq+ *qqq+ *qqq qqq+'.replace(/\*q+\+/g, '!'));
// ---> *+ ! ! ! *qqq qqq+

