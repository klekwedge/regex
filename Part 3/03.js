// С помощью replace замените в строке домены вида 
// http://site.ru, http://site.com на 
// <a href="http://site.ru">site.ru</a>.

console.log('http://site.ru'.replace(/^(http:\/\/([a-z.-_]+\.[a-z]{2,3}))$/, '<a href="$1">$2</a>'));
// ---> <a href="http://site.ru">site.ru</a>

console.log('http://example.com'.replace(/^(http:\/\/([a-z.-_]+\.[a-z]{2,3}))$/, '<a href="$1">$2</a>'));
// ---> <a href="http://example.com">example.com</a>

