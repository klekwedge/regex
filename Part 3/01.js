// Дана строка вида 'a1b2c3'. 
// Напишите регулярку, которая 
// найдет все цифры и удвоит их количество таким образом: 
// 'a11b22c33' (то есть рядом с каждой цифрой напишет такую же)

console.log('a1b2c3'.replace(/([0-9]+)/g, '$1$1'))
// ---> a11b22c33


