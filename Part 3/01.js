// Дана строка 'aaa@bbb eee7@kkk'. 
// Напишите регулярку, которая найдет строки по шаблону: 
// любое количество букв и цифр, символ @, любое количество 
// букв и цифр и поменяет местами то, что стоит до @ на то, 
// что стоит после нее. Например, aaa@bbb должно превратиться в bbb@aaa.

console.log('aaa@bbb eee7@kkk'.replace(/([a-z0-9]+)@([a-z0-9]+)/g, '$2@$1'))
// ---> bbb@aaa kkk@eee7

