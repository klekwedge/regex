// Дана строка 'aa aba abba abbba abbbba abbbbba'. 
// Напишите регулярку, которая найдет строки abba, abbba, abbbba и только их

console.log('aa aba abba abbba abbbba abbbbba'.replace(/ab{2,4}a/g, '!'));
// ---> aa aba ! ! ! abbbbba

