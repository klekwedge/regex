//////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////

// Дана строка 'aAXa aeffa aGha aza ax23a a3sSa'. 
// Напишите регулярку, которая найдет строки следующего 
// вида: по краям стоят буквы 'a', а между ними - маленькие 
// и большие латинские буквы, не затронув остальных.

console.log('aAXa aeffa aGha aza ax23a a3sSa'.replace(/a[a-zA-Z]+a/g, '!'));
// ---> ! ! ! ! ax23a a3sSa

